export function DuotoneIcon({
  name,
  size = 18,
  className,
  strokeColor,
  fillColor,
  accentColor,
}: DuotoneIconProps) {
  const icon = iconPaths[name];
  if (!icon) {
    console.warn(`DuotoneIcon: Unknown icon "${name}"`);
    return null;
  }

  // Default colors - stroke is currentColor (inherits from parent)
  // Accent uses CSS variable --primary or passed accentColor
  const effectiveStroke = strokeColor || "currentColor";
  const effectiveFill = fillColor || effectiveStroke;
  const effectiveAccent = accentColor || "var(--primary)";

  // Parse multiple paths from stroke string (separated by " M")
  const parsePathString = (pathStr: string) => {
    if (!pathStr) return [];
    // Split by "M" but keep the M
    return pathStr.split(/(?=M)/).filter(Boolean).map(p => p.trim());
  };

  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      width={size}
      height={size}
      viewBox="0 0 18 18"
      className={cn("flex-shrink-0", className)}
      fill="none"
    >
      {/* Accent fill (background) */}
      {icon.accent && (
        <path
          d={icon.accent}
          fill={effectiveAccent}
          fillOpacity={accentColor ? 1 : 0.3}
        />
      )}

      {/* Solid fill elements (dots, etc) */}
      {icon.fill && parsePathString(icon.fill).map((path, i) => (
        <path
          key={`fill-${i}`}
          d={path}
          fill={effectiveFill}
        />
      ))}

      {/* Stroke paths */}
      {icon.stroke && parsePathString(icon.stroke).map((path, i) => (
        <path
          key={`stroke-${i}`}
          d={path}
          stroke={effectiveStroke}
          strokeWidth="1.5"
          strokeLinecap="round"
          strokeLinejoin="round"
          fill="none"
        />
      ))}
    </svg>
  );
}
